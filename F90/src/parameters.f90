   INTEGER :: vent_flag

   !> Flag to crop the DEM
   !> - T      => crop
   !> - F      => no crop
   !> .
   LOGICAL :: crop_flag

   !> Flag to save the hazard file
   !> - T      => save hazard file
   !> - F      => no save
   !> .
   LOGICAL :: hazard_flag

