   REAL(wp), ALLOCATABLE :: Ytopo(:,:)


   INTEGER ::  iW, iE, jS, jN

   REAL(wp) :: xcmin, xcmax, ycmin, ycmax

   REAL(wp) :: pi
   REAL(wp) :: deg2rad
   REAL(wp) :: rad2deg

   REAL(wp), ALLOCATABLE :: X_circle(:)
   REAL(wp), ALLOCATABLE :: Y_circle(:)

   REAL(wp), ALLOCATABLE :: source_probabilities(:)

   LOGICAL :: force_max_length
   LOGICAL :: start_from_dist_flag
   INTEGER :: max_length

   REAL(wp), ALLOCATABLE :: masking_threshold(:)
   INTEGER :: n_masking


CONTAINS

   !***********************************************************************
   !> @brief Initialize angular constants and the discretized unit circle.
   !!
   !! Computes the constants `pi`, `deg2rad`, and `rad2deg`, allocates the
   !! auxiliary arrays used to represent a discretized unit circle, and
   !! fills them with cosine and sine values.
   !!
   !! The subroutine builds the parameter array `t` with `npoints`
   !! uniformly spaced values between `0` and `2*pi`, then uses it to
   !! initialize `X_circle` and `Y_circle`. These arrays are later used to
   !! construct discretized ellipse boundaries in the flow computations.
   !!
   !! @date 2026-04-02
   !! @author M. de' Michieli Vitturi
   !! @author S. Tarquini
   !!
   !! @note This subroutine has no explicit input or output arguments.
   !! It operates on module variables defined in `parameters`.
   !***********************************************************************
   SUBROUTINE init_run

      IMPLICIT NONE

      REAL(wp), ALLOCATABLE :: t(:)
      REAL(wp) :: dt

      INTEGER :: i

      pi = 4.0_wp * ATAN(1.0_wp)
      deg2rad = pi / 180.0_wp
      rad2deg = 180.0_wp / pi

      ! Allocate arrays
      allocate(t(npoints))
      allocate(X_circle(npoints))
      allocate(Y_circle(npoints))

      ! Compute the step size
      dt = 2.0_wp * pi / (npoints - 1)

      ! Generate the values for t
      do i = 1, npoints
         t(i) = (i - 1) * dt
      end do

      ! Compute X_circle and Y_circle
      X_circle = cos(t)
      Y_circle = sin(t)

   END SUBROUTINE init_run


END MODULE parameters

