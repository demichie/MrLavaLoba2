MODULE parameters

   IMPLICIT NONE

   INTEGER, PARAMETER :: sp = kind(1.0)
   INTEGER, PARAMETER :: dp = kind(1.0d0)

   !> working precision
   INTEGER, PARAMETER :: wp = dp

   REAL(wp), PARAMETER :: eps = epsilon(1.0_wp)


   CHARACTER(LEN=50) :: run_name
   CHARACTER(LEN=50) :: source

